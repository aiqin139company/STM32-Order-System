/*
	函数功能：显示图片：240x320
	参数：*p图片地址
*/
void LCD_Pic(const unsigned char * p)
{
	int x,y;	
	
	LCD_SetCursor(0x00,0x0000);	//设置光标位置 
	LCD_WriteRAM_Prepare();     //开始写入GRAM	 	  
	
	for(y=0;y<320;y++){
    for(x=0;x<240;x++){
			LCD_WR_DATA((*p<<8)|(*(p+1)));
			p += 2;			
    }			
	}
} 

