/*
	函数功能：读取个某点的颜色值	 
	参数：u16x,u16y:坐标
	返回值:此点的颜色
*/
u16 LCD_ReadPoint(u16 x,u16 y)
{
 	u16 r=0,g=0,b=0;
	if(x>=LCDDEV.width||y>=LCDDEV.height) return 0;	//超过了范围,直接返回		   
	LCD_SetCursor(x,y);	    
	LCD_WR_REG(0X2E);							//9341发送读GRAM指令
	if(DataIn)	r=0;							//dummy Read	   
	opt_delay(2);	  
 	r = DataIn;  		  					//实际坐标颜色
	opt_delay(2);	  
	b = DataIn; 
	g = r&0XFF;										//对于9341,第一次读取的是RG的值,R在前,G在后,各占8位
	g <<= 8;
	return (((r>>11)<<11)|((g>>10)<<5)|(b>>11));	//ILI9341需要公式转换一下
}

/*
	函数功能：转换RGB格式
	参数：	u16 c:GBR格式的颜色值
	返回值：RGB格式的颜色值
	说明：  从ILI93xx读出的数据为GBR格式，而我们写入的时候为RGB格式。
				  通过该函数转换
*/
u16 LCD_BGR2RGB(u16 c)
{
	u16  r,g,b,rgb;   
	b=(c>>0)&0x1f;
	g=(c>>5)&0x3f;
	r=(c>>11)&0x1f;         
	rgb=(b<<11)+(g<<5)+(r<<0);                 
	return(rgb);
}                 

