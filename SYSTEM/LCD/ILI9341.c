/*
	函数功能：圆心画直线
	参数：int x 圆心横坐标，
				int y 圆心纵坐标，
				int r 半径，直线的长度
				int du 度数
				int color 画点颜色
*/
void Draw_Circle_Line(int x,int y,int r,int du,int color)
{
	int a,b;
	float k;
	k = (du-90)*(3.14159/180);		//转换成弧度制
	a = cos(k)*r;									//求出x轴坐标偏移
	b = sin(k)*r;									//求出y轴坐标偏移
	POINT_COLOR = color;					//设置画点颜色
	LCD_DrawLine(x,y,x+a,y+b);		//调用画线函数
}

/*
	函数功能：画带颜色的能自定义粗的圆
	参数：int x 圆心横坐标，
				int y 圆心纵坐标，
				int r 半径，直线的长度
				int q 圈数
				int color 画点颜色
*/
void Draw_Circles(int x,int y,int r,int q,int color)
{
	while(q--)
		Draw_Circle(x,y,r+q,color);			//画圆
}

/*
	函数功能：以圆圈的自由角度画虚点
	参数：int x 圆心横坐标，
				int y 圆心纵坐标，
				int r 半径，直线的长度
				int q 圈数
				int du 角度
				int color 画点颜色
*/
void Draw_Circle_Dot(int x,int y,int r,int q,int du,int color)
{
	int a=0,b=0;
	float k;
	POINT_COLOR = color;								//设置画点颜色				
	k = (du-90)*(3.14159/180);					//转换成弧度制
	a = cos(k) * r;											//求出x轴坐标偏移
	b = sin(k) * r;											//求出y轴坐标偏移
	Draw_Circles(x+a,y+b,0,q,color);//画小点	
}

