/*
	函数功能：设置LCD的自动扫描方向
	说明：注意:其他函数可能会受到此函数设置的影响
				所以,一般设置为L2R_U2D即可,如果设置为其他扫描方式,可能导致显示不正常.
	参数：u8 dir:0~7,代表8个方向(具体定义见lcd.h)   
*/
void LCD_Scan_Dir(u8 dir)
{
	u16 regval=0;
	u8 dirreg=0;
	u16 temp;  
	if(LCDDEV.dir==1){			   
		switch(dir){//方向转换
			case 0 : dir=6; break;
			case 1 : dir=7; break;
			case 2 : dir=4; break;
			case 3 : dir=5; break;
			case 4 : dir=1; break;
			case 5 : dir=0; break;
			case 6 : dir=3; break;
			case 7 : dir=2; break;	     
		}
	}
		switch(dir){
			case L2R_U2D://从左到右,从上到下
				regval|=(0<<7)|(0<<6)|(0<<5); 
				break;
			case L2R_D2U://从左到右,从下到上
				regval|=(1<<7)|(0<<6)|(0<<5); 
				break;
			case R2L_U2D://从右到左,从上到下
				regval|=(0<<7)|(1<<6)|(0<<5); 
				break;
			case R2L_D2U://从右到左,从下到上
				regval|=(1<<7)|(1<<6)|(0<<5); 
				break;	 
			case U2D_L2R://从上到下,从左到右
				regval|=(0<<7)|(0<<6)|(1<<5); 
				break;
			case U2D_R2L://从上到下,从右到左
				regval|=(0<<7)|(1<<6)|(1<<5); 
				break;
			case D2U_L2R://从下到上,从左到右
				regval|=(1<<7)|(0<<6)|(1<<5); 
				break;
			case D2U_R2L://从下到上,从右到左
				regval|=(1<<7)|(1<<6)|(1<<5); 
				break;	 
		}
		dirreg=0X36;
 		regval|=0X08;//BGR   
		LCD_WriteReg(dirreg,regval);
 		if((regval&0X20)||LCDDEV.dir==1){
			if(LCDDEV.width<LCDDEV.height){	//交换X,Y
				temp=LCDDEV.width;
				LCDDEV.width=LCDDEV.height;
				LCDDEV.height=temp;
 			}
		}else{
			if(LCDDEV.width>LCDDEV.height){//交换X,Y
				temp=LCDDEV.width;
				LCDDEV.width=LCDDEV.height;
				LCDDEV.height=temp;
 			}
		}  
		LCD_WR_REG(LCDDEV.setxcmd); 
		LCD_WR_DATA(0);
		LCD_WR_DATA(0);
		LCD_WR_DATA((LCDDEV.width-1)>>8);
		LCD_WR_DATA((LCDDEV.width-1)&0XFF);
		
		LCD_WR_REG(LCDDEV.setycmd); 
		LCD_WR_DATA(0);LCD_WR_DATA(0);
		LCD_WR_DATA((LCDDEV.height-1)>>8);LCD_WR_DATA((LCDDEV.height-1)&0XFF);  
		LCD_WriteReg(dirreg,regval);
}

/*
	函数功能：设置LCD显示方向
	参数：u8 dir:0,竖屏；1,横屏
*/
void LCD_Display_Dir(u8 dir)
{
	if(dir==0){			//竖屏
		LCDDEV.dir=0;	//竖屏
		LCDDEV.width=LCDWIDTH;
		LCDDEV.height=LCDHEIGHT;
		LCDDEV.wramcmd=0X2C;
	 	LCDDEV.setxcmd=0X2A;
		LCDDEV.setycmd=0X2B;  	 
	}else{ 					//横屏			
		LCDDEV.dir=1;	//横屏
		LCDDEV.width=LCDHEIGHT;
		LCDDEV.height=LCDWIDTH;
		LCDDEV.wramcmd=0X2C;
	 	LCDDEV.setxcmd=0X2A;
		LCDDEV.setycmd=0X2B;  	 
	} 
	LCD_Scan_Dir(DFT_SCAN_DIR);	//默认扫描方向
}	

